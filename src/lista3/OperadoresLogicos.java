package lista3;

public class OperadoresLogicos {
    static void main() {
        int a = 1;
        int b = 2;
        int c = 3;
        int d = 4;
        System.out.println(a > b || c < d);
        System.out.println(a > b || c < d);
        System.out.println(a > b && c < d);
        System.out.println(a > b && c < d);
        System.out.println(a > b || c < d);
    }
}
