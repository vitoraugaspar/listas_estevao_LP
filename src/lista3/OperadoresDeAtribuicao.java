package lista3;

public class OperadoresDeAtribuicao {
    static void main() {
        int a = 1;
        System.out.println(a);
        a += 2;
        System.out.println(a);
        a -= 1;
        System.out.println(a);
        a *= 3;
        System.out.println(a);
        a /= 2;
        System.out.println(a);
        a -= 2;
        System.out.println(a);
        a++;
        System.out.println(a);
        a--;
        System.out.println(a);
    }
}
