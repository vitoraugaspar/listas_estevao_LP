package lista2;

public class ExibeValores {
    static void main() {
        byte inteiroUm = 9;
        short inteiroDois = 1571;
        int inteiroTres = 100000;
        long longNumero = 2147483648L;
        float doubleNumero = 3.14F;
        double doubleNumero2 = 3.141592653589793;
        boolean verdadeiroOuFalso = true;
        char charUm = 'K';
        System.out.println(inteiroUm + ", " + inteiroDois + ", " + inteiroTres  + ", " + longNumero + ", "
                + doubleNumero  + ", " + doubleNumero2  + ", " + verdadeiroOuFalso  + ", " +  charUm );

    }
}
//9, 1571, 100000, 2147483648, 3.14, 3.141592653589793, true e 'K'.